import java.util.*;

public class relationalop {
    public static void main(String args[]) {
        int a = 34;
        int b = 54;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a >= b);

    }
}
